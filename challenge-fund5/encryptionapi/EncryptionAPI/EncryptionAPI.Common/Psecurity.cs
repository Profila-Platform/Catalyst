        public static string AsymetricKeyEncryption(string plainText, string publicKey)
        {
            var csp = new RSACryptoServiceProvider();
            csp.FromXmlString(publicKey);
            //csp.ImportParameters(GetRSAParameters(publicKey));
            var data = Encoding.Unicode.GetBytes(plainText);
            var cypher = csp.Encrypt(data, false);
            return Convert.ToBase64String(cypher);
        }
        public static string AsymetricKeyDecryption(string cypher, string privateKey)
        {
            var csp = new RSACryptoServiceProvider();
            var dataBytes = Convert.FromBase64String(cypher);
            csp.FromXmlString(privateKey);
            var plaintext = csp.Decrypt(dataBytes, false);
            return Encoding.Unicode.GetString(plaintext);
